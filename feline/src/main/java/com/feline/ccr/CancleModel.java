package com.feline.ccr;

public class CancleModel {

	
	private int cancle_num;
	private int order_num;
	private String cancle_reason;
	private String member_id;
	
	
	
	public int getCancle_num() {
		return cancle_num;
	}
	public void setCancle_num(int cancle_num) {
		this.cancle_num = cancle_num;
	}
	public int getOrder_num() {
		return order_num;
	}
	public void setOrder_num(int order_num) {
		this.order_num = order_num;
	}
	public String getCancle_reason() {
		return cancle_reason;
	}
	public void setCancle_reason(String cancle_reason) {
		this.cancle_reason = cancle_reason;
	}
	public String getMember_id() {
		return member_id;
	}
	public void setMember_id(String member_id) {
		this.member_id = member_id;
	}
	
}
