package com.feline.wishlist;

public class WishListModel {
	
	
	private int wish_num;
	private String member_id;
	private String goods_num;
	private String goods_name;
	private String goods_image_savname;
	private String goods_size;
	private long goods_price;
	
	public int getWish_num() {
		return wish_num;
	}
	public void setWish_num(int wish_num) {
		this.wish_num = wish_num;
	}
	public String getMember_id() {
		return member_id;
	}
	public void setMember_id(String member_id) {
		this.member_id = member_id;
	}
	public String getGoods_num() {
		return goods_num;
	}
	public void setGoods_num(String goods_num) {
		this.goods_num = goods_num;
	}
	public String getGoods_name() {
		return goods_name;
	}
	public void setGoods_name(String goods_name) {
		this.goods_name = goods_name;
	}
	public String getGoods_image_savname() {
		return goods_image_savname;
	}
	public void setGoods_image_savname(String goods_image_savname) {
		this.goods_image_savname = goods_image_savname;
	}
	public String getGoods_size() {
		return goods_size;
	}
	public void setGoods_size(String goods_size) {
		this.goods_size = goods_size;
	}
	public long getGoods_price() {
		return goods_price;
	}
	public void setGoods_price(long goods_price) {
		this.goods_price = goods_price;
	}
	
	
}
